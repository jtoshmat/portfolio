                var scat = $target.attr('data-subcategory');
                if(scat){
                    $('[data-subcategory="'+scat+'"]').addClass('active').parent().addClass('active');
                }
                var focus = $target.prop('focus');
                if(focus){
                    try {
                        $(focus).focus();
                    }catch(e){

                    }
                }
                if(typeof slide_callbacks[id] == 'function'){
                    slide_callbacks[id]();
                }
                if(typeof callback == 'function'){
                    callback();
                }
            }
        }


