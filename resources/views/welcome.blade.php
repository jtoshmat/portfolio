        function sumData(data, prependCol){
            var ret = [];
            ret[0] = data[0].slice(1);
            ret[1] = []
            for(var r=1; r<data.length; r+=1){
                for(var c=1; c<data[r].length; c+=1){
                    if( typeof ret[1][c-1] == 'undefined' ){
                        ret[1][c-1] = 0;
                    }
                    ret[1][c-1] += data[r][c];
                }
            }
            if(typeof prependCol != 'undefined'){
                for(var i=0; i<prependCol.length; i+=1){
                    ret[i].unshift(prependCol[i]);
                }
            }
            return ret;
        }

        function pivotData(data, cols){
            var ret = [];
            if(typeof cols == 'undefined'){
                cols = [];
                for(var i in data[0]){
                    cols.push(i);
                }
            }
            for(var r=0; r<data.length; r+=1){
                for(var c in cols){
                    if( typeof ret[c] == 'undefined' ){
                        ret[c] = [];
                    }
                    ret[c][r] = data[r][cols[c]];
                }
            }
            return ret;
        }

        function getRowData(data, rows){
            //console.log('getting row data ', rows);
            var ret = [];
            ret.push(data[0].slice(0));
            if(typeof rows == 'number'){
                ret.push(data[rows].slice(0));
            }else{
                for(var i=0; i<rows.length; i+=1){
                    ret.push(data[rows[i]].slice(0))
                }
            }
            return ret;
        }
