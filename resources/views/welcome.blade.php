                $target.show();
                $('.active').removeClass('active');
                $('[href="'+id+'"]').addClass('active').parent().addClass('active');
                var cat = $target.attr('data-category');
                if(cat){
                    $('[data-category="'+cat+'"]').addClass('active').parent().addClass('active');
